## ETP species #####

### Whale sharks ####
WHALE_SHARK = query(C_MASTER, "SELECT SPECIES_CATEGORY_CODE, [ORDER], CODE, NAME_EN, NAME_SCIENTIFIC FROM refs_biological.V_SPECIES WHERE CODE LIKE 'RHN'")

### Turtles ####
TURTLE_SPECIES = query(C_MASTER, "SELECT SPECIES_CATEGORY_CODE, [ORDER], CODE, NAME_EN, NAME_SCIENTIFIC FROM refs_biological.V_SPECIES WHERE SPECIES_CATEGORY_CODE = 'TURTLES' AND IS_AGGREGATE = 'false'")

### Cetaceans ####
CETACEAN_SPECIES = query(C_MASTER, "SELECT SPECIES_CATEGORY_CODE, [ORDER], CODE, NAME_EN, NAME_SCIENTIFIC FROM refs_biological.V_SPECIES WHERE SPECIES_CATEGORY_CODE = 'CETACEANS' AND IS_AGGREGATE = 'false'")

### Seabirds ####
SEABIRD_SPECIES = query(C_MASTER, "SELECT SPECIES_CATEGORY_CODE, [ORDER], CODE, NAME_EN, NAME_SCIENTIFIC FROM refs_biological.V_SPECIES WHERE SPECIES_CATEGORY_CODE = 'SEABIRDS' AND IS_AGGREGATE = 'false'")

ETP_SPECIES = rbindlist(list(WHALE_SHARK, TURTLE_SPECIES, CETACEAN_SPECIES, SEABIRD_SPECIES))

